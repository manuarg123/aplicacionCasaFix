    private void CalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalcularActionPerformed
    aireAcondicionado ventanaReparacion;
    ventanaReparacion= new aireAcondicionado();
    
    ventanaReparacion.setChoice_cGas(opt_cGasT.isSelected());
    ventanaReparacion.setChoice_cCapacitor(opt_cCapT.isSelected());
    ventanaReparacion.setChoice_cComp(opt_cCompT.isSelected());
    ventanaReparacion.setChoice_cPlaca(opt_cPlacaT.isSelected());
    ventanaReparacion.setChoice_cRemoto(opt_cRemoto.isSelected());
    ventanaReparacion.setChoice_Orep(opt_OrepT.isSelected());
    
   
    
    lbl_orep.setText(String.valueOf(ventanaReparacion.solicitarPresupuesto()));
    lbl_repPresT.setText(String.valueOf(ventanaReparacion.presupuestoReparacion()));
    }//GEN-LAST:event_CalcularActionPerformed

